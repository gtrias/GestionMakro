    public void activarModoCreacion(){
        limpiarCampos();
        jbNuevo.setVisible(false);
        tfCodCliente.setEnabled(true);
        modoConsulta = false;
        jbPDia.setVisible(false);
        jbCancelar.setVisible(true);
        tfNombre.requestFocus();
    }
    public void activarModoConsulta(){
        limpiarCampos();
        tfCodCliente.setText("");
        tfCodCliente.setEnabled(true);
        modoConsulta = true;
        jbPDia.setVisible(true);
        jbCancelar.setVisible(false);
    }
