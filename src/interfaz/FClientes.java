    public void guardarCliente(){
        
       if(ControlClientes.obtenerInstancia().nuevoCliente(tfNombre.getText(), tfNif.getText(),tfCalle.getText(),tfCP.getText(),tfTipo.getText(),cbRecargo.isSelected(), tfActividad.getText())){
            System.out.println("Cliente añadido correctamente");
       }else{
           System.out.println("Error al añadir cliente");
       }
    }
    
    
    public void mostrarCliente(Cliente c){
        limpiarCampos();
        
        tfCodCliente.setText(c.getCodCliente());
        tfNif.setText(c.getNif());
        tfNombre.setText(c.getNombre());
        tfActividad.setText(c.getCodActividad().getNombre());
        tfFecha.setText(c.getFechaAlta().toString());
        tfCalle.setText(c.getDireccion());
        tfCP.setText(c.getCodigoPostal());
        
        tfBloqueo.setText(ControlClientes.obtenerInstancia().obtenerMensajeBloqueo(c));
        mostrarComprador1(c);
        mostrarComprador2(c);
        cbBloqueo.setSelectedItem(ControlClientes.obtenerInstancia().obtenerCodBloqueo(c));
        //tfTipo.setText(c.getCodTipo().getNombre());
        //cbRecargo.setSelected(c.getRecargo());
    }
    
    public void limpiarCampos(){
        // CAMPOS GENERALES
        tfNif.setText("");
        tfNombre.setText("");
        tfActividad.setText("");
        tfFecha.setText("");
        tfBloqueo.setText("");
        tfCalle.setText("");
        tfCP.setText("");
        cbBloqueo.setSelectedIndex(0);
        // CAMPOS DE COMPRADOR
        nombreComp1.setText("");
        apellidosComp1.setText("");
        telefonoComp1.setText("");
        movilComp1.setText("");
        faxComp1.setText("");
        nombreComp2.setText("");
        apellidosComp2.setText("");
        telefonoComp2.setText("");
        movilComp2.setText("");
        faxComp2.setText("");
    }
    
    public void rellenarBloqueos(){
        List bloqueos = ControlClientes.obtenerInstancia().obtenerListaBloqueos();
        for(int i=0; i< bloqueos.size(); i++){
            Bloqueo b = (Bloqueo) bloqueos.get(i);
            cbBloqueo.addItem(b.getTipo()+" : "+b.getDescripcion());
        }
    }
    
